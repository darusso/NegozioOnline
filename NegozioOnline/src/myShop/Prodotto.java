package myShop;

public class Prodotto {
    
    public int id;
    public String nome;
    public String descrizione;
    public double prezzo;

    public Prodotto(int id, String nome, String descrizione, double prezzo) {
        this.id = id;
        this.nome = nome;
        this.descrizione = descrizione;
        this.prezzo = prezzo;
    }
    
    
    
    
}